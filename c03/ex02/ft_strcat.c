char	*ft_strcat(char *dest, char *src)
{
	int i;
	int n;

	i = 0;
	n = 0;
	while (dest[i] != '\0')
		i++;
	if (dest[i] == '\0')
	{
		while (src[n] != '\0')
		{
			dest[i] = src[n];
			i++;
			n++;
		}
	}
	dest[i] = '\0';
	return (dest);
}
